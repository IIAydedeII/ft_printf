#include "../includes/ft_printf.h"

int	ft_putstr(char *s)
{
	if (!s)
		return (-1);
	return ((int)write(1, s, ft_strlen(s)));
}

int	ft_print_str(char *s)
{
	if (!s)
		return (ft_putstr("(null)"));
	return (ft_putstr(s));
}
